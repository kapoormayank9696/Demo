// Character 1-D Array Algorithm Implementation In Java

public class Character_1D {
    // Main function
    public static void main(String[] args) {
        char[] charArray = {'H', 'e', 'l', 'l', 'o'};     
        System.out.println("\nCharacter 1-D Array:");
        for (char c : charArray) {
            System.out.print(c + " ");
        }
    }
}

